from util.from_type import *


class DayOfWeek(CaseInsensitiveEnum):
    """Gets or sets the day of week.
    
    An enum that represents a day of the week, weekdays, weekends, or all days.
    """
    EVERYDAY = "Everyday"
    FRIDAY = "Friday"
    MONDAY = "Monday"
    SATURDAY = "Saturday"
    SUNDAY = "Sunday"
    THURSDAY = "Thursday"
    TUESDAY = "Tuesday"
    WEDNESDAY = "Wednesday"
    WEEKDAY = "Weekday"
    WEEKEND = "Weekend"


